# 람다 함수란 👉 이름 없는 한 줄짜리 함수
# lambda 매개변수: 반환값

# 일반 함수 vs 람다
def add(a, b):
    return a + b

add = lambda a, b: a + b
print(add(1, 2))  # 3

# 특징 (중요)
# 이름 없이 사용 가능
# 한 줄만 가능
# return 키워드 없음 (자동 반환)

# 바로사용(핵심 패턴)
print((lambda a, b: a + b)(1, 2))


# 정렬
data = [("정호", 30), ("철수", 20), ("영희", 25)]

data.sort(key=lambda x: x[0])

print(data)


