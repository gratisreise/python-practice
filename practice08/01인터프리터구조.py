# 1️⃣ Python 인터프리터 구조 (전체 흐름)

# 한 줄 요약부터 가자 👇

# 👉 Python은 “소스코드 → 바이트코드 → 가상머신 실행” 구조다

# 흐름은 이렇게 된다:

# Python 코드 (.py)
#         ↓
# 컴파일 (Parser + Compiler)
#         ↓
# 바이트코드 (.pyc)
#         ↓
# Python Virtual Machine (PVM)
#         ↓
# 실행
# 2️⃣ 단계별로 쪼개서 이해
# ① 소스코드 (우리가 작성)
# print("hello")

# 👉 이건 그냥 텍스트 파일이다 (아직 실행 불가능)

# ② 컴파일 단계 (인터프리터 내부)

# 여기서 중요한 포인트 👇

# 👉 파이썬도 “컴파일”을 한다 (하지만 머신코드가 아님)

# 내부 과정:

# Lexical Analysis (토큰화)

# print, (, "hello", )
# Parsing (문법 분석)
# → AST (Abstract Syntax Tree) 생성
# 컴파일
# → 바이트코드로 변환
# ③ 바이트코드 생성

# 👉 결과: .pyc 파일 (캐시)

# __pycache__/xxx.cpython-311.pyc

# 👉 특징

# CPU가 직접 실행 못함
# Python 전용 중간 코드
# ④ Python Virtual Machine (핵심)

# 👉 여기서 진짜 실행됨

# 바이트코드를 한 줄씩 읽음
# 스택 기반으로 동작

# 예:

# LOAD_CONST
# PRINT_ITEM

# 👉 즉,
# 👉 “인터프리터 = PVM 실행기”라고 보면 된다

# 3️⃣ 핵심 구조 한방 정리

# 👉 Python 인터프리터는 3가지로 나눠 기억해라

# 1. Parser

# → 코드 구조 이해 (문법 체크)

# 2. Compiler

# → 바이트코드 생성

# 3. Virtual Machine (PVM)

# → 실행

# 4️⃣ 왜 이 구조가 중요한가 (실무 관점)

# 너 백엔드니까 이 포인트 중요하다 👇

# ✔ 빠른 실행처럼 보이는 이유
# .pyc 캐싱 → 재실행 시 컴파일 스킵
# ✔ 플랫폼 독립성
# 바이트코드 → 어디서든 동일하게 실행
# ✔ 성능 한계
# PVM이 한 줄씩 실행 → 느림
# 5️⃣ 한 줄 면접 답변

# 👉
# “Python은 소스코드를 바로 실행하는 것이 아니라, 먼저 바이트코드로 컴파일한 뒤 Python Virtual Machine이 이를 실행하는 인터프리터 구조입니다.”

